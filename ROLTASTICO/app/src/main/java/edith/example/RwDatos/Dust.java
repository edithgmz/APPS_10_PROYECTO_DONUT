package edith.example.RwDatos;

public class Dust {
    private int iDust1;
    private int iDust2;

    public Dust(int iDust1, int iDust2) {
        this.iDust1 = iDust1;
        this.iDust2 = iDust2;
    }

    public int getiDust1() {
        return iDust1;
    }

    public void setiDust1(int iDust1) {
        this.iDust1 = iDust1;
    }

    public int getiDust2() {
        return iDust2;
    }

    public void setiDust2(int iDust2) {
        this.iDust2 = iDust2;
    }
}
